  const constants = require("../../services/nftConstants.json") 


const getSortAttributeString = (category,index) => {
  if(index>0 && index<5)
    return constants.RacingAttributeFieldNames[constants.RacingAttributeFields[category][index-1]]
  return "Default"
}


const getSeasonString = (season) => {
  return constants.Seasons[season]
}
const getCollectionString = (collection) => {
  return constants.Collections[collection]
}
const getRarityString = (rarity) => {
  return constants.Rarities[rarity]
}
const capitalize = (str) => {
  return str.charAt(0).toUpperCase() + str.slice(1)
}

const getRarityStringCapitalized = (rarity) => {
  return capitalize(constants.Rarities[rarity])
}

const getTileGlowUrlForRarity = (rarity) =>{
  return tileGlowUrls[rarity]
}
const getStatMaxForItemType = (type) => {
  switch(type){
    case "car":
      return 1001
    case "driver":
      return 1001
    default:
      return 600
  }
}
const getStatMaxForLoadout = (type) => {
  switch(type){
    case "car":
      return getStatMaxForItemType("car") + (9*getStatMaxForItemType())
    case "driver":
      return getStatMaxForItemType("driver") + (4*getStatMaxForItemType())
  }
}
const getColorForRarity = (rarity) => {
    switch(rarity)
    {
      case "common":
        return "#ACACAC";
      case "epic":
        return "#B584FF";
      case "legendary":
        return "#F6BC65";
      case "rare":
        return "#0A8C4B";
      case "apex":
        return "#01FFF7";
    }
  }

  const getSubTypesInCategory = (category) =>{
    return category == "car"?["powerunit","turbocharger","frontwing","rearwing","energystore","brakes","transmission","suspension"]:
    ["gloves","suit","helmet","boots"]
  }
  const getUnequippedImageUrlForItemType = (type, season=2019) => {
    return season ? unequippedItemImageUrls[season][type] : '';
  }
  const itemTypeToLabel = (type) => {
    const types = {
      car:"Car",
      driver:"Driver",
      powerunit:"Power Unit",
      turbocharger:"Turbo Charger",
      frontwing:"Front Wing",
      rearwing:"Rear Wing",
      energystore:"Energy Store",
      brakes:"Brakes",
      transmission:"Transmission",
      suspension:"Suspension",
      gloves:"Gloves",
      suit:"Suit",
      helmet:"Helmet",
      boots:"Boots"}
    return types[type]
  }
  
  const getPreviewItemHaloUrl = (type,rarity,fullbody) => {
    const rarityType = getRarityString(rarity)
    switch(type){
      case "car":return carHaloUrls[rarityType]
      case "driver":return fullbody?driverFullHaloUrls[rarityType]:driverHaloUrls[rarityType]
    }
    return partHaloUrls[rarityType]
  }

  const getNewPreviewItemHaloUrl = (type,rarity,fullbody) => {
    const rarityType = getRarityString(rarity)
    switch(type){
      case "car":return newCarHaloUrls[rarityType]
      case "driver":return fullbody?newDriverFullHaloUrls[rarityType]:driverHaloUrls[rarityType]
    }
    return partHaloUrls[rarityType]
  }

